package com.mercurio.game.pokemon;

public class timerData {
    private int first;
    private int second;
    private int third;

    public timerData(int first, int second, int third) {
        this.first = first;
        this.second = second;
        this.third = third;
    }
    
    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }

    public int getThird() {
        return third;
    }
}
